package cust;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity 
public class Customer {
	@Id
    private int cid;
	private String name;
    private int age;
    private long phno;
    public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public long getPhno() {
		return phno;
	}
	public void setPhno(long phno) {
		this.phno = phno;
	}
	
    
}
